package Models;

/**
 *
 * @author Jorge
 */
public class Item {
    private int id;
    public String nombre;

    public Item() {
    }

    public Item(int id, String nombre) {
        this.id = id;
        this.nombre = nombre;
    }

    public int getId() {
        return id;
    }

    @Override
    public String toString() {
        return nombre;
    }
}



